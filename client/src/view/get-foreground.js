/// Public ///
/**
 * Converts foreground hash to SVG file name and degree of clockwise rotation to apply.
 * @param {string} hash Tile edge types from top going clockwise, represented as a string of six numbers.
 * @returns {[svg, rotation]} Array with SVG file name in first element and degree of rotation in second element.
 */
const getForeground = (hash) => {
  const svg = foregroundConversion[hash][0];
  const deg = foregroundConversion[hash][1] * 60;

  return [svg, deg];
}

export default getForeground;
